public class Person {
    String first_Name;
    String last_Name;
    String address;
    String city;
    String state;
    String zip;
    String phone_Number;
    String email;

   public Person(String first_Name, String last_Name, String address, String city,
                 String state, String zip, String phone_Number, String email){

       this.first_Name = first_Name;
       this.last_Name = last_Name;
       this.address = address;
       this.city = city;
       this.state = state;
       this.zip = zip;
       this.phone_Number = phone_Number;
       this.email = email;

   }
}
